public class CustomKeyValue {
    private final int first;
    private final int second;
    private final String date;

    public CustomKeyValue(int first, int second, String date) {
        this.first = first;
        this.second = second;
        this.date = date;
    }

    public int getFirst() {
        return first;
    }
    public String getDate() {
        return date;
    }
    public int getSecond() {
        return second;
    }
}
